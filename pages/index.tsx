import Head from "next/head";
import styles from "@/styles/Home.module.css";
import { useEffect } from "react";
import { IncomeTable } from "@/views/IncomeTable";
import { ExpenseTable } from "@/views/ExpenseTable";
import { Settings } from "@/views/Settings";
import { CalcsTable } from "@/views/CalcsTable";
import {
  fetchCalcsFx,
  fetchCalcsHeadersFx,
  fetchExpenseHeadersFx,
  fetchExpensesFx,
  fetchIncomeHeadersFx,
  fetchIncomesFx,
  fetchInitialBalancesFx,
} from "@/effects/getDataFx";
import { autosaveFx, calcTableUpdateFx } from "@/effects/autosaveFx";
import { sample } from "effector";
import { setCalcs } from "@/events/calcs";
import { autosaveTimer } from "@/events/autosave";
import { $calcs, $expenses, $incomes, $initialBalances } from "@/stores/calcs";
import { Accordion, AccordionDetails, AccordionSummary } from "@mui/material";
import { UploadRow } from "@/views/UploadRow";
import { useSession } from "next-auth/react";
import Link from "next/link";

sample({
  clock: autosaveTimer,
  source: {
    calcs: $calcs, expenses: $expenses, incomes: $incomes, balances: $initialBalances
  },
  fn: ({calcs, expenses, incomes, balances}) => ({calcs, expenses, incomes, balances}),
  target: autosaveFx,
});

sample({
  clock: setCalcs,
  target: calcTableUpdateFx,
});

export default function Home() {
  const { data } = useSession();
  console.log('data', data);
  
  useEffect(() => {
    fetchInitialBalancesFx();
  }, []);

  useEffect(() => {
    fetchCalcsFx();
  }, []);

  useEffect(() => {
    fetchCalcsHeadersFx();
  }, []);

  useEffect(() => {
    fetchIncomesFx();
  }, []);

  useEffect(() => {
    fetchIncomeHeadersFx();
  }, []);

  useEffect(() => {
    fetchExpensesFx();
  }, []);

  useEffect(() => {
    fetchExpenseHeadersFx();
  }, []);

  return data ? (
    <>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className={styles.main}>
        <Link href="/api/auth/signout">Выход</Link>
        <UploadRow ignoreAuth />
        <Accordion>
          <AccordionSummary>Постоянные доходы и расходы</AccordionSummary>
          <AccordionDetails>
            <div
              style={{
                display: "flex",
                flexDirection: "column",
                rowGap: "16px",
              }}
            >
              <IncomeTable />
              <ExpenseTable />
            </div>
          </AccordionDetails>
        </Accordion>
        <Settings />
        <CalcsTable />
      </main>
    </>
  ) : (
    <Link href="/api/auth/signin">Логин</Link>
  );
}
